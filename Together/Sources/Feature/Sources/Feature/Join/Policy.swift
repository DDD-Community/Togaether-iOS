    허위 또는 과장된 정보 입력 행위
    회사의 서비스 이용 시 회원이 입력하는 정보 일체에 대하여 허위 또는 과장된 정보를 입력 하는 행위
    자기 또는 타인에게 재산상의 이익을 주거나 타인에게 손해를 가할 목적으로 허위의 정보를 유통시키는 행위
    회사가 운영하는 플랫폼 내에 상호(성명), 로고 등 상표, 주소, 전화번호, 이메일 주소 등 신원 식별을 용이하게 하는 정보를 회사의 사전 동의 내지 허락을 얻지 아니하고 입력 내지 사용하는 행위
    공공질서 위반, 성별 · 정치 · 종교 · 장애 · 연령 · 사회적 신분 · 인종 · 지역 · 직업 등을 차별하거나 이에 대한 편견을 조장하는 행위
    회사 또는 다른 회원, 제3자를 차별 또는 비방하거나 욕설, 폭언, 협박, 명예를 훼손하는 행위
    과도한 신체 노출이나 음란한 행위를 묘사하거나, 성매매 관련 정보를 공유하거나, 타인에 게 성적 수치심이나 불쾌감을 유발할 수 있는 내용을 게시하는 등 미풍양속에 반하는 정보를 등 록하는 행위
    혐오스러운 사진 또는 내용을 게시하거나, 욕설, 비속어, 은어를 사용하는 등 사회 통념에 반하는 비정상적인 활동을 하는 행위
    타인의 권리 침해 · 회사 및 다른 회원, 제3자의 특허권, 상표권, 저작권 등 지적재산권 을 침해하는 행위 또는 침해할 우려가 있는 행위
    타인의 개인정보, 프라이버시(Privacy)를 침해하는 행위
    기타 웹 해킹 프로그램, 매크로 프로그램, 보이스 피싱을 위한 미러링 사이트 등 타인의 권 리를 침해하거나 침해할 우려가 있는 모든 행위
    계정 및 계정 내 정보를 타인에게 판매, 양도, 대여하거나, 타인에게 그 이용을 허락 또 는 이를 시도하는 행위 및 이를 알선하는 행위
    명의 사칭, 도용 등으로 다른 회원, 제3자 또는 회사의 직원을 사칭하는 행위
    회사의 이익(영업)을 저해하는 행위
    회사의 동의 없이 영리, 영업, 광고, 정치활동 등을 목적으로 서비스를 사용하는 행위
    관련 법령에서 금지하거나 선량한 풍속 기타 사회통념상 허용되지 아니하거나 원활한 서비스 제공에 악영향을 끼치는 행위
    기타 위 각 호에 준하는 행위
    제23조 (양도금지)
    회원은 본 약관에 의한 서비스의 이용권한, 서비스 이용계약 상의 권리, 지위 등을 타인에게 양도 및 증여할 수 없으며, 이를 담보로 제공할 수 없습니다.
    제24조 (회원의 의무위반에 대한 제재 등)
    ① 회사는 회원이 본 약관상의 내용을 위반한 경우, 사안의 중요성 및 다른 회원, 제3자, 회 사가 입은 불이익 내지 손해를 고려하여 위반 회원에게 다음과 같은 제재를 할 수 있습니다.
    단순 경고 : 회사는 본 약관상의 의무를 위반한 회원에게 의무위반의 내용, 위반 시기 등 관련 내용을 고지하고, 이에 대한 위반을 하지 않을 것을 경고하는 조치를 취할 수 있습니다. 경고 이후에도 동일한 의무 위반이 [2]회 이상 반복되는 경우에는 회사는 해당 회원의 서비스 이용을 제한하거나 이용계약을 해지할 수 있습니다.
    시정조치 요청 : 회사는 회원의 의무위반 사실 및 이에 대한 시정조치를 함께 요청할 수 있 습니다. 해당 회원이 시정조치에 대한 내용을 이행하지 않은 상태에서 발생한 불이익 내지 손해 에 대해서는 회사는 면책됩니다. 회사의 시정조치에도 불구하고, 시정되지 않는 경우에는 회사는 해당 회원의 계정을 삭제할 수 있습니다.
    서비스 이용 제한 : 회사는 일정한 기간 또는 서비스의 범위를 정하여 회원의 서비스 이용을 제한할 수 있습니다. 이 경우 회원은 회사가 제한한 범위 내에서는 회원으로서의 권리를 행사 할 수 없습니다.
    서비스 이용계약 해지 : 회사는 해당 회원이 서비스를 이용하지 못하도록 서비스 이용계약을 일시적 또는 영구적으로 해지**(계정삭제)**할 수 있습니다.
    기타 회사가 필요하다고 인정하는 조치 내지 제재
    ② 회사는 본 조에 따른 제재 조치를 하는 과정에서 해당 회원의 이메일, 플랫폼 내 알림 등을 통해 개별적으로 고지할 수 있습니다.
    ③ 회원은 제2항의 고지를 받은 날로부터 7 일 이내에 회사의 제재 내용에 대하여 소명을 할 수 있으며, 플랫폼 내에 게시된 고객센터 또는 플랫폼에 게시된 회사의 이메일 주소를 통하여 소명이 담긴 내용을 회사에 전달합니다.
    ④ 회사는 제3항에 따라 제출된 소명 내용에 대하여 제출된 증빙자료를 토대로 형식적인 심사 만 진행하며, 실질적인 내용에 심사는 진행하지 않습니다. 따라서 소명의견을 전달한 회원은 회사의 심사 내용에 대하여 이의를 제기하거나 실질적인 내용에 대한 심사까지 요구할 수 없습니다.
    ⑤ 회원이 본 약관의 내용을 위반하여 회사 또는 제3자(다른 회원 포함)에게 손해가 발생한 경 우 회원은 그로 인하여 발생한 손해를 배상해야 합니다.
    제6장 저작권 등에 관한 내용
    제25조 (회원의 저작권)
    본 약관에 따라 회원이 직접 제작하여 플랫폼에 게시 내지 업로드한 콘텐츠(부호, 문자, 도형,색채, 음성, 음향, 음원, 이미지 및 영상 등(이들의 복합체를 포함한다), 본 약관에서 동일) 에 대한 저작권은 해당 회원이 소유하며, 회사는 플랫폼 내에서 서비스의 원활한 제공을 위한 범위 내에서 해당 콘텐츠를 게시(변형, 가공, 요약, 발췌, 취합 행위를 포함)할 수 있는 권리를 갖습니다.
    제26조 (회원이 제작한 콘텐츠 등에 대한 관리방안)
    ① 회사는 회원이 플랫폼에 게시하는 콘텐츠가 다음 각 호에 해당하는 경우 플랫폼 게시를 금지합니다.
    서비스 이용 목적에 부합하지 않는 경우
    제3자의 개인정보를 직접 기재하는 등으로 제3자의 권리를 침해하는 경우
    회사의 플랫폼을 통하지 않고 직접 거래를 유도하거나 권유, 제안하는 경우
    회사 또는 서비스와 관련하여 객관적인 사실과 부합하지 않거나 관련이 없는 경우(ex. 회 원의 개인적인 인격이나 명예 등을 훼손하는 내용이 담긴 콘텐츠, 정당한 근거 없이 회사의 서 비스를 폄하하거나 신용을 저하시키는 내용이 담긴 콘텐츠 등)
    본 약관에 위배되거나 불법, 음란, 저속하다고 판단되는 내용인 경우
    다른 회원 또는 제3자를 비방하거나 명예를 훼손시키는 내용인 경우
    공공질서 및 공서양속에 위반되는 내용인 경우
    범죄적 행위에 결부된다고 인정되는 내용일 경우
    제3자의 초상권, 저작권 등 기타 권리를 침해하는 내용인 경우
    기타 관계 법령에 위배되는 경우
    위 각 호에 준하는 경우
    ② 제1항 각 호의 어느 하나에 해당하는 내용의 콘텐츠가 플랫폼에 게시된 경우, 회사는 임의 로 삭제, 비활성화 등 필요한 조치를 할 수 있습니다.
    ③ 회사는 회원이 다른 회원 또는 제3자의 저작권을 침해하는 경우, 저작권법 등 관계법령에 정해진 절차에 의하여 저작권자 또는 회원의 요청에 의해 해당 콘텐츠를 삭제하거나 다시 게시 할 수 있으며, 이 경우 회사는 면책됩니다. 회원이 다른 회원 또는 제3자의 저작권 등의 권리 를 침해함으로써 발생하는 민, 형사상의 책임은 침해한 회원이 전적으로 부담합니다.
    제27조 (회사의 저작권 등의 권리 및 이에 따른 회원의 의무)
    ① 회사가 제작한 콘텐츠에 대한 저작권 및 기타 지식재산권은 회사의 소유입니다. 회사는 서비 스와 관련하여 회사가 정한 이용조건에 따라 서비스를 이용할 수 있는 이용권한만을 회원에게 부 여하며, 회원은 서비스를 이용하여 얻은 정보를 가공, 판매하는 행위 등 플랫폼에 게재된 콘텐 츠를 상업적으로 사용할 수 없으며, 이를 유상양도, 판매, 담보제공 등의 처분행위를 할 수 없 습니다.
    ② 회원은 회사가 제공하는 서비스를 이용함으로써 얻은 정보 중 회사 또는 제3자에게 지적재산 권 등이 귀속된 정보를 회사 또는 제3자의 사전 승낙 없이 복제, 전송, 출판, 배포, 방송 기 타 방법에 의하여 이용하거나 제3자에게 이용하게 하여서는 안 됩니다. 이 경우, 영리, 비영리 이용을 모두 포함한다.
    ③ 회사가 제공하는 서비스, 그에 필요한 소프트웨어, 이미지, 마크, 로고, 디자인, 서비스 명칭, 정보 및 상표 등과 관련된 지식재산권 및 기타 권리는 회사에게 있습니다. 회원은 회사 가 명시적으로 승인한 경우를 제외하고는 상기에 명시한 소정의 각 재산에 대한 전부 또는 일부 의 수정, 대여, 대출, 판매, 배포, 제작, 양도, 재라이센스, 담보권설정행위 등 일체의 상업적 이용행위를 할 수 없으며, 제3자로 하여금 이와 같은 행위를 하도록 허락할 수 없습니다.
    제7장 서비스 이용계약의 종료
    제28조 (계정삭제)
    ① 회원은 언제든지 플랫폼을 통하여 서비스 이용계약의 해지 신청(계정삭제)을 할 수 있습니 다. 이 경우 회사는 회원의 본인여부 확인 등 필요한 절차를 요구할 수 있으며, 신속히 처리하 도록 노력합니다.
    ② 제1항의 경우, 회사는 계정삭제(‘탈퇴’라고도 함) 회원의 정보를 별도의 보관 법령 또는 회사 내부 규정이 있는 경우를 제외하고 즉시 삭제합니다.
    ③ 회원은 계정삭제 후, 동일한 계정(휴대폰 번호)를 통하여 재가입할 수 있습니다. 다만, 본 약관에 따른 회사의 제재 조치에 따른 계정 삭제 시에는 해당 회원의 계정은 재가입 가능한 대 상에 포함되지 않을 수 있습니다.
    ④ 회사는 회원의 계정이 삭제된 경우(회원 스스로 자신의 계정을 삭제한 경우 또는 회사가 회 원의 계정을 본 약관에 따라 삭제한 경우 모두 포함), 해당 회원이 플랫폼에 게시한 콘텐츠를 삭제할 수 있습니다.
    ⑤ 회사는 유료서비스 이용 회원이 탈퇴하는 경우, 유료서비스 이용에 대한 이력 등이 삭제할 수 있습니다. 이 경우 해당 회원이 보유한 젤리는 자동으로 소멸되어 복구되지 않습니다 약관 제17조 제8항 참조).
    제29조 (회사의 해지)
    ① 회사는 회원이 본 약관의 내용을 위반한 경우 또는 다음 각 호의 어느 하나에 해당하는 경우에는 7일 이내의 범위에서 기간을 정하여 시정할 것을 최고하고, 그 기간 동안 시정되지 않을 경우 서비스 이용계약을 해지할 수 있습니다. 다만, 회원이 현행법 위반 및 고의 또는 중대한 과실로 회사에 손해를 입힌 경우에는 사전통보 없이 서비스 이용계약을 해지할 수 있습니다.
    회원이 제공한 데이터가 허위임이 판명된 경우
    범죄적 행위에 관련되는 경우
    국익 또는 사회적 공익을 저해할 목적으로 서비스 이용을 계획 또는 실행할 경우
    타인의 서비스 계정 및 비밀번호를 도용한 경우
    타인의 명예를 손상시키거나 불이익을 주는 경우
    서비스의 건전한 이용을 저해하는 경우
    기타 관련 법령이나 회사가 정한 이용조건에 위배되는 경우
    ② 회사가 특정 회원과의 서비스 이용계약을 해지하는 경우 회사는 해당 회원에게 해지사유 및 해지일을 기재한 서면, 전자우편, 플랫폼 내 알림 또는 이에 준하는 방법으로 회원에게 통보합니다.
    제8장 회사의 책임제한 등
    제30조 (회사의 책임제한)
